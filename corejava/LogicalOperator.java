public class LogicalOperator {
    public static void main(String[] args) {
        
        int a=5; int b=10; int c=15;
        //&& operator
        System.out.println((a>b) &&(b>c));
      //  || operator
        System.out.println((a<b) || (b<c));
        // ! operator
        System.out.println(!(a==c));
        System.out.println(!(b>a));


    }
    
}
