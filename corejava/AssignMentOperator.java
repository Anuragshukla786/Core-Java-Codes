public class AssignMentOperator {
    public static void main(String[] args) {
        
        int p=10; 
        int q;

        // =
         q=p;

         System.out.println(q);

         //+=

         p+=q;
         System.out.println(p);

         // -=q
         p-=q;
         System.out.println(p);
         // *=
         p*=q;
         System.out.println(p);
         // /=
         p/=q;
         System.out.println(p);
         //%=
         p%=q;
         System.out.println(p);
    }
    
}
